    public void enableNext() {
        String entryM = nombreMaharaja.getText().replaceAll("\\s+","");
        String entryC = nombreCipayos.getText().replaceAll("\\s+","");
        
        if (!entryC.isEmpty() && !entryM.isEmpty() && 
            !"Acabacontodounejército...".equals(entryM) && 
            !"Derrotaalrebelde...".equals(entryC) &&
            !entryC.equals(entryM) && buttonGroup1.getSelection() != null) {
            jButton1.setEnabled(true); 
        } 
        else { jButton1.setEnabled(false); }
    }
    
