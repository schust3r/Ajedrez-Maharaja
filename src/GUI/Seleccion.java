    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        dispose();

        String jugCipayos  = nombreCipayos.getText();
        String jugMaharaja = nombreMaharaja.getText();
        
        int x = Integer.parseInt(posInicial.split(",")[0]);
        int y = Integer.parseInt(posInicial.split(",")[1]);
        
        ExecutorService executor = Executors.newCachedThreadPool();
        
        try {
            executor.execute(new Juego(false, false, jugCipayos, jugMaharaja, x, y));
        } 
        catch (Exception e) { }
        
    }//GEN-LAST:event_jButton1ActionPerformed

